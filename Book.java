class Book {
    private String title;
    private String author;
    private String genre;
    private int pages;
    private String dateRead;

    public Book(String title, String author, String genre, int pages, String dateRead) {
        this.title = title;
        this.author = author;
        this.genre = genre;
        this.pages = pages;
        this.dateRead = dateRead;
    }

}
